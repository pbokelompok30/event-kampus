            txtPassword.setText(tbladmin.getModel().getValueAt(row, 2).toString());
            txtInstansi.setText(tbladmin.getModel().getValueAt(row, 3).toString());
            txtNamaAdmin.setText(tbladmin.getModel().getValueAt(row, 4).toString());
            txtNoHp.setText(tbladmin.getModel().getValueAt(row, 5).toString());
