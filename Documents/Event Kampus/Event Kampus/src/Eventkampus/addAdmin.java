    public void nama (KeyEvent p){
        if(txtNamaAdmin.getText().length()<3  && txtNamaAdmin.getText().length()>100){
        p.consume();
        JOptionPane.showMessageDialog(null, "maksimal panjang namaAdmin adalah 100 karakter dan minimal 3 karakter", "peringatan", JOptionPane.WARNING_MESSAGE);
    }
    }
      public void instansi (KeyEvent p){
        if(txtInstansi.getText().length()<3  && txtInstansi.getText().length()>100){
        p.consume();
        JOptionPane.showMessageDialog(null, "maksimal panjang Instansi adalah 100 karakter dan minimal 3 karakter", "peringatan", JOptionPane.WARNING_MESSAGE);
    }
    }
    
        public void username (KeyEvent p){
        if(txtUsername.getText().length()>15  || Character.isSpaceChar(p.getKeyChar())){
        p.consume();
        JOptionPane.showMessageDialog(null, "maksimal panjang password 15 karakter dan tidak ada spasi", "peringatan", JOptionPane.WARNING_MESSAGE);
    }
    }
        public void password(KeyEvent p){
        if(txtPassword.getText().length()>10 || Character.isSpaceChar(p.getKeyChar())){
        p.consume();
        JOptionPane.showMessageDialog(null, "maksimal panjang password 10 karakter dan tidak ada spasi", "peringatan", JOptionPane.WARNING_MESSAGE);
    }
    }
        public void nohp (KeyEvent p){
        if(txtNoHp.getText().length()>13 || Character.isAlphabetic(p.getKeyChar())|| Character.isSpaceChar(p.getKeyChar())){
        p.consume();
        JOptionPane.showMessageDialog(null, "Maksimal karakter 13 tidak boleh ada huruf ataupun spasi", "peringatan", JOptionPane.WARNING_MESSAGE);
        }
    }
        public void idadmin (KeyEvent p){
        if(txtIdAdmin.getText().length()>11 || Character.isSpaceChar(p.getKeyChar()) || Character.isAlphabetic(p.getKeyChar())){
        p.consume();
        JOptionPane.showMessageDialog(null, "Maksimal karakter 11 tidak boleh ada huruf ataupun spasi", "peringatan", JOptionPane.WARNING_MESSAGE);    
        }
        }
